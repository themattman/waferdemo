nv.addGraph(function() {
  var chart  = nv.models.multiBarChart()
    , chart2 = nv.models.multiBarChart()
  ;

  chart.xAxis
      .axisLabel('Trial (n)')
      .tickFormat(d3.format(',r'));

  chart2.xAxis
      .axisLabel('Trial (n)')
      .tickFormat(d3.format(',r'));

  chart.yAxis
      .axisLabel('Time (ms)')
      .tickFormat(d3.format(',r'));

  chart2.yAxis
      .axisLabel('Time (ms)')
      .tickFormat(d3.format(',r'));

  d3.select('#chart-local svg')
      .datum(data())
      .transition().duration(500)
      .call(chart);

  d3.select('#chart-cloud svg')
      .datum(data2())
      .transition().duration(500)
      .call(chart2);

  nv.utils.windowResize(chart.update);
  nv.utils.windowResize(chart2.update);

  return chart, chart2;
});

